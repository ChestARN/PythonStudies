#Showing the types of the variable x
x = 5
print(type(x)) #Integer(int)

x = 'Hello World'
print(type(x)) #String(str)

x = 3.5
print(type(x)) #Float(float)

x = ['apple, banana, cherry']
print(type(x)) #List(list)

x = ('apple, banana, cherry')
print(type(x)) #Tuple(tuple)

x = {'apple, banana, cherry'}
print(type(x)) #Dictionary(dict)

x = True
print(type(x)) #Boolean(bool)
