fruits = ["apple", "banana", "cherry"]
print(fruits[1]) #Printing the second component of the list.

fruits[0] = "kiwi" #Changing the first component of the list.

fruits.append("orange") #Adding another component to the end of the list.

fruits.insert(1, "lemon") #Inserting a new item in position 1.

fruits.remove("banana") #Removing banana fron the list.

print(fruits[-1]) #Printing the last item of the list.

fruits = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(fruits[2:5]) #Printing the the third, fourth, and fifth item in the list.

print(len(fruits)) #Printing the number of items in the list.
